      <section class="article__section">
        <h2>Le moral, un état en perpétuelle évolution</h2>

        <p>
          La déprime est un mal-être passager qui fait partie de la vie ordinaire. Elle survient
          à l'occasion de difficultés, d'événements ou même sans raison apparente, voire juste
          de manière saisonnière.
        </p>
        <p>
          En revanche, la dépression est une maladie. En 2017, la dépression a concerné près de
          10&nbsp;% des Français. Les populations les plus touchées sont les personnes de moins de
          45&nbsp;ans, les femmes, les chômeurs et les personnes inactives, les personnes veuves ou
          divorcées, ou les personnes déclarant de faibles revenus. À noter également&nbsp;: 8&nbsp;%
          des personnes en emploi ont connu un épisode dépressif en 2017.
        </p>
        <p>
          La dépression s'explique par divers facteurs psychologiques et liés à l'environnement
          social ou familial.
        </p>
        <p>
          Certains facteurs interviennent très en amont&nbsp;; ils «&nbsp;préparent le terrain&nbsp;».
          Par exemple, le risque d'être touché par la dépression serait accru par le fait de vivre
          des événements traumatisants ou des conflits parentaux importants pendant la petite enfance.
        </p>
        <p>
          D'autres facteurs interviennent juste avant la dépression, ils la «&nbsp;déclenchent&nbsp;»&nbsp;:
          mort d'un être cher, naissance d'un enfant, séparation, conflit familial, annonce d'une
          maladie grave, perte de son emploi, harcèlement à l'école ou au travail, etc.
        </p>
        <p>
          Certains psychiatres conçoivent la dépression comme une profonde fatigue psychique, en
          particulier chez les personnes qui luttent depuis des années contre les diverses formes
          d'anxiété (anxiété généralisée, phobies, troubles paniques ou troubles obsessionnels
          compulsifs — TOC) ou contre les effets d'une mauvaise estime d'eux-mêmes.
        </p>
        <p>
          Certains symptômes de la déprime et de la dépression sont identiques&nbsp;: fatigue, perte
          de concentration, troubles du sommeil, tristesse par exemple. Mais ceux de la déprime sont
          plus modérés et tendent à disparaître spontanément avec le temps et les activités sociales.
        </p>
        <p>
          Dans le cas d'une dépression, ces symptômes se manifestent toute la journée et presque
          tous les jours, durant au moins deux semaines, et ils ne sont pas influencés par les
          circonstances.
        </p>
        <p>
          D'autres symptômes peuvent venir s'y ajouter&nbsp;: perte ou gain de poids, troubles
          somatiques (maux de ventre ou de dos, par exemple), désespoir, voire désir d'en finir
          avec la vie. La personne touchée a un sentiment d'inutilité et d'impuissance, avec des
          idées morbides, voire suicidaires.
        </p>

        <div class="article__warning">
          <p>
            Le suicide est le risque principal lié à la dépression. Il peut être prévenu par un
            traitement adapté de la maladie.
          </p>
        </div>
