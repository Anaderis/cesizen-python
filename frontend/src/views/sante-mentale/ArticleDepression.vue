        <h2>À qui en parler&nbsp;?</h2>
        <p>
          Rester isolé sans parler de son état dépressif ne fait qu'aggraver la situation. Dire ce
          que l'on ressent quand on va mal, cela fait partie d'un processus naturel qui permet
          d'aller mieux. Il est donc primordial d'oser en parler à une personne de confiance. Cela
          peut être un proche, son médecin généraliste, un spécialiste de la santé psychique
          (psychologue, psychiatre), une association spécialisée, dans le cadre d'un groupe de
          parole, etc.
        </p>
        <p>
          On peut également le faire de manière anonyme par l'intermédiaire d'un chat sur internet,
          d'une application mobile, ou d'une ligne d'écoute dédiée aux troubles psychiques.
        </p>

        <div class="article__callout">
          <p>
            En cas de pensée suicidaire, parlez-en à un professionnel de santé ou à une ligne
            d'écoute. <strong>En cas d'urgence, appelez le&nbsp;15.</strong>
          </p>
        </div>
      </section>

      <!-- Section 3 -->
      <section class="article__section">
        <h2>Qui consulter&nbsp;?</h2>
        <p>
          La dépression est une maladie qui se soigne. Un traitement est nécessaire car la volonté
          seule ne suffit pas pour agir sur une maladie aussi complexe. Psychothérapie et/ou
          médicaments&nbsp;: il existe aujourd'hui des traitements efficaces, souvent
          complémentaires, adaptés à chaque personne et à l'intensité de la maladie.
        </p>

        <div class="article__cards">
