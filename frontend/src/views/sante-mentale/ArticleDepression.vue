      <!-- Section 5 -->
      <section class="article__section">
        <h2>Prendre soin de soi accélère la guérison</h2>
        <p>
          Entretenir des liens sociaux est essentiel lorsqu'on souffre d'un état dépressif. Ce
          n'est pas forcément simple car la dépression incite à se replier sur soi et à se
          considérer comme «&nbsp;indigne&nbsp;» ou «&nbsp;incapable&nbsp;» d'avoir des relations
          satisfaisantes. Il est donc essentiel de profiter des périodes de rémission pour voir
          ses amis, sa famille, ses collègues, et participer à des activités caritatives,
          culturelles, sportives, artistiques.
        </p>
        <p>
          Pratiquer régulièrement certaines activités physiques (marche rapide, vélo, natation)
          contribue à réduire les symptômes des dépressions légères à modérées. La pratique en
          groupe ou en club associe les bienfaits de l'activité physique à ceux de l'échange avec
          d'autres personnes.
        </p>
        <p>
          Se relaxer grâce à différentes techniques (sophrologie, méditation, yoga) peut réduire
          les tensions du corps ainsi que la «&nbsp;rumination&nbsp;» et les idées noires. Pour être
          efficaces, ces approches nécessitent un temps d'apprentissage (de préférence avec un
          professionnel) et une pratique régulière.
        </p>
